/**
 * op_add - adds two inegers
 * @a: the first int
 * @b: the second int
 *
 * Return: the sum
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - subtracts two ingegers
 * @a: the first int
 * @b: the second int
 *
 * Return: the fifference
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_div - divides two integers
 * @a: the first int
 * @b: the second int
 *
 * Return: the quotient
 */
int op_div(int a, int b)
{
	return (a / b);
}
